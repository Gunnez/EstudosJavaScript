n1 = 4
n2 = 48

console.log(n1>n2)  //Maior que
console.log(n1<n2)  //Menor que
console.log(n1>=n2)  //Maior ou igual que
console.log(n1<=n2)  //Menor ou igual que
console.log(n1==4)  //Igual a
console.log(n2!=4)  //diferente 
console.log(n1==='4')   //Igual a (com o mesmo tipo que)
console.log(n1!=='4')   //diferente (com tipos diferentes)

